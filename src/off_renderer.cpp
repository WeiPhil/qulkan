static std::vector<char> readFile(const std::string &filename) {
    // Open the file in read binary mode
    std::ifstream file(filename, std::ios::ate | std::ios::binary);
    if (!file.is_open()) {
        std::cout << filename << std::endl;
        throw std::runtime_error("failed to open file!");
    }

    // Store the file's content in the buffer
    size_t fileSize = (size_t)file.tellg();
    std::vector<char> buffer(fileSize);
    file.seekg(0);
    file.read(buffer.data(), fileSize);

    if (buffer.size() != fileSize) {
        throw std::runtime_error("failed to read shader in file!");
    }

    // Close the file and return
    file.close();
    return buffer;
}

static bool hasStencilComponent(VkFormat format) { return format == VK_FORMAT_D32_SFLOAT_S8_UINT || format == VK_FORMAT_D24_UNORM_S8_UINT; }

